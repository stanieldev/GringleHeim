// Loading Assets For World
void World::loadFloorAtlas() {
    sf::Texture tempTexture;
    std::vector<std::string> floorMap = {
        "grassssssssss.png", // 0
        "woody.png",         // 1
        "stonislav.png",     // 2
        "marble_time.png",   // 3
    };
    for (unsigned int i = 0; i < floorMap.size(); i++) {
        tempTexture.loadFromFile("assets\\blocks\\" + floorMap.at(i));
        floorAtlas.push_back(tempTexture);
    }
}
void World::loadWallAtlas() {
    sf::Texture tempTexture;
    std::vector<std::string> wallMap = {
        ".png",     // 0000
        "L.png",    // 0001
        "R.png",    // 0010
        "RL.png",   // 0011
        "D.png",    // 0100
        "DL.png",   // 0101
        "DR.png",   // 0110
        "DRL.png",  // 0111
        "U.png",    // 1000
        "UL.png",   // 1001
        "UR.png",   // 1010
        "URL.png",  // 1011
        "UD.png",   // 1100
        "UDL.png",  // 1101
        "UDR.png",  // 1110
        "UDRL.png", // 1111
    };
    for (unsigned int i = 0; i < wallMap.size(); i++) {
        tempTexture.loadFromFile("assets\\walls\\" + wallMap.at(i));
        wallAtlas.push_back(tempTexture);
    }
}
World::World() {
    loadFloorAtlas();
    loadWallAtlas();
}



// Player Map Render
void World::genWorld(sf::RenderWindow& window, Player player) {
    // Default Temporary Sprite
    sf::Sprite tempSprite;
    tempSprite.setScale(2, 2);

    // Map & Texture Indexing
    int mapX, mapY;
    int posX, posY;

    // Make Background Default Sky Color
    window.clear(sf::Color::Cyan);

    // Loops For All Blocks On Screen
    int tempIndex;
    for (int i = -render.renderDistanceX; i < render.renderDistanceX; i++) {
    for (int j = -render.renderDistanceY; j < render.renderDistanceY; j++) {

            // Finding Indeces
            mapX =  player.blockX + i;
            mapY =  player.blockY + (render.renderDistanceY + j);
            posX = -player.pixelX + (render.screenResolutionX / 2) + (i * 32);
            posY = -player.pixelY + (render.screenResolutionY / 2) + (j * 32);

            /*mapX = player.blockX + (render.renderDistanceX + i);
            mapY = player.blockY + (render.renderDistanceY + j);
            posX = -player.pixelX + (render.screenResolutionX / 2) + (i * 32);
            posY = -player.pixelY + (render.screenResolutionY / 2) + (j * 32);*/

            // Draw Block(s)
            if (mapX > -1 && mapX < mapMaxX && mapY > -1 && mapY < mapMaxY) { // If Within Bounds Of Map Array

                if (wallArray[mapY][mapX] == 1) { // If Wall
                    tempIndex = 15;
                    if (wallArray[mapY][mapX - 1] == 1) { tempIndex -= 1; }
                    if (wallArray[mapY][mapX + 1] == 1) { tempIndex -= 2; }
                    if (wallArray[mapY + 1][mapX] == 1) { tempIndex -= 4; }
                    if (wallArray[mapY - 1][mapX] == 1) { tempIndex -= 8; }
                    tempSprite.setTexture(wallAtlas.at(tempIndex));
                }
                else                            // If Not Wall
                    tempSprite.setTexture(floorAtlas.at(floorArray[mapY][mapX]));

                tempSprite.setPosition(posX, posY);
                window.draw(tempSprite);

            }

            //tempSprite.setTexture(floorAtlas.at(3));
            //tempSprite.setPosition(player.pixelX + (render.screenResolutionX / 2) + 16, player.pixelY + (render.screenResolutionY / 2) + 16);
            //window.draw(tempSprite);
    }}


}


